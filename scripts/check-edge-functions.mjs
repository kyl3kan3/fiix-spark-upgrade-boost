import { spawnSync } from "node:child_process";
import { readFile, readdir } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { parse } from "smol-toml";

const root = process.cwd();
const functionsRoot = path.join(root, "supabase", "functions");
const configPath = path.join(root, "supabase", "config.toml");

const config = parse(await readFile(configPath, "utf8"));
const expectedJwtSettings = {
  "authorize-public-request": false,
  "submit-public-request": false,
  "payments-webhook": false,
  "resend-webhook": false,
  "create-checkout-context": true,
  "paddle-portal": true,
  "update-seats": true,
  "mcp": false,
};

for (const [functionName, expected] of Object.entries(expectedJwtSettings)) {
  const actual = config.functions?.[functionName]?.verify_jwt;
  if (actual !== expected) {
    throw new Error(
      `${functionName} must set verify_jwt = ${expected}; received ${String(actual)}`,
    );
  }
}

const entries = await readdir(functionsRoot, { withFileTypes: true });
const entrypoints = entries
  .filter((entry) => entry.isDirectory() && !entry.name.startsWith("_"))
  .map((entry) => path.join("supabase", "functions", entry.name, "index.ts"));

const strictEntrypoints = [];
const generatedEntrypoints = [];
for (const entrypoint of entrypoints) {
  let source;
  try {
    source = await readFile(entrypoint, "utf8");
  } catch {
    // Some function folders contain only shared modules.
    continue;
  }

  if (source.startsWith("// AUTO-GENERATED by @lovable.dev/mcp-js")) {
    if (/npm:[A-Za-z]:[\\\\/]/.test(source)) {
      throw new Error(`${entrypoint} contains a machine-local Windows npm import.`);
    }
    generatedEntrypoints.push(entrypoint);
  } else {
    strictEntrypoints.push(entrypoint);
  }
}

const denoProbe = spawnSync("deno", ["--version"], { stdio: "ignore" });
const useInstalledDeno = denoProbe.status === 0;
const npmCli = process.env.npm_execpath;
if (!useInstalledDeno && !npmCli) {
  throw new Error("Deno is not installed and npm_execpath is unavailable.");
}

function checkEntrypoints(configFile, files) {
  if (files.length === 0) return;

  const command = useInstalledDeno ? "deno" : process.execPath;
  const checkArgs = ["check", "--config", path.join("supabase", "functions", configFile), ...files];
  const args = useInstalledDeno
    ? checkArgs
    : [npmCli, "exec", "--yes", "--", "deno", ...checkArgs];
  const result = spawnSync(command, args, { cwd: root, stdio: "inherit" });
  if (result.error) throw result.error;
  if (result.status !== 0) process.exit(result.status ?? 1);
}

checkEntrypoints("deno.json", strictEntrypoints);
checkEntrypoints("deno.generated.json", generatedEntrypoints);

console.log(
  `Checked ${strictEntrypoints.length} strict and ${generatedEntrypoints.length} generated Edge Function entrypoints plus JWT settings.`,
);
